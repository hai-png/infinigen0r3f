/**
 * Get the number of triangles for a given configuration
 */
export function getTriangleCount(configuration: number): number {
  if (configuration < 0 || configuration >= 256) return 0;

  let count = 0;
  const base = configuration * 16;
  for (let i = 0; i < 16; i += 3) {
    if (TRIANGLE_TABLE[base + i] === -1) break;
    count++;
  }
  return count;
}

/**
 * Check if an edge is intersected for a given configuration
 */
export function isEdgeIntersected(configuration: number, edgeIndex: number): boolean {
  if (configuration < 0 || configuration >= 256) return false;
  if (edgeIndex < 0 || edgeIndex >= 12) return false;
  return (EDGE_TABLE[configuration] & (1 << edgeIndex)) !== 0;
}

/**
 * Get triangle vertices for a configuration
 */
export function getTriangleVertices(configuration: number): number[] {
  if (configuration < 0 || configuration >= 256) return [];

  const vertices: number[] = [];
  const base = configuration * 16;

  for (let i = 0; i < 16; i++) {
    const vertex = TRIANGLE_TABLE[base + i];
    if (vertex === -1) break;
    vertices.push(vertex);
  }

  return vertices;
}
