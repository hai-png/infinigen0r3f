/**
 * Negated relation: NOT(inner).
 *
 * Satisfies when the inner relation does NOT satisfy.
 * Implements De Morgan's laws for intersection/difference.
 */
export class NegatedRelation extends SpatialRelation {
  readonly type: RelationType = RelationType.TOUCHING; // Placeholder; negation is meta

  constructor(
    /** The inner relation being negated */
    public readonly inner: SpatialRelation
  ) {
    super();
  }

  implies(other: SpatialRelation): boolean {
    // NOT(A) implies NOT(B) if B implies A (contrapositive)
    // i.e., if B => A, then NOT(A) => NOT(B)
    if (other instanceof NegatedRelation) {
      return other.inner.implies(this.inner);
    }
    return false;
  }

  satisfies(point: THREE.Vector3, state: any): boolean {
    return !this.inner.satisfies(point, state);
  }

  intersects(other: SpatialRelation): SpatialRelation | null {
    // De Morgan: NOT(A) ∩ NOT(B) = NOT(A ∪ B)
    // For now, we return a simplified result
    if (other instanceof NegatedRelation) {
      // NOT(A) ∩ NOT(B) = NOT(A OR B)
      // Represented as NegatedRelation — we'd need an OrRelation for full fidelity
      // For simplicity, return the more restrictive negation
      return new NegatedRelation(other.inner);
    }
    // NOT(A) ∩ B = B \ A
    return other.difference(this.inner);
  }

  difference(other: SpatialRelation): SpatialRelation | null {
    // NOT(A) \ NOT(B) = NOT(A) ∩ B = B \ A
    if (other instanceof NegatedRelation) {
      return this.inner.difference(other.inner);
    }
    // NOT(A) \ B = NOT(A ∪ B)
    return new NegatedRelation(other);
  }

  getConstraints(): GeometricConstraint[] {
    // Negated constraints are open intervals — hard to represent as bounds
    // Return unbounded constraints (the solver must handle negation separately)
    return [{ min: -Infinity, max: Infinity }];
  }

  clone(): NegatedRelation {
    return new NegatedRelation(this.inner.clone());
  }
}

