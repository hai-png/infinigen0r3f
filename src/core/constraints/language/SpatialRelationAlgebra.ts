/**
 * Factory function to create a SpatialRelation from a RelationType and parameters.
 *
 * @param type - The relation type to create
 * @param params - Optional parameters specific to each relation type
 * @returns A new SpatialRelation instance
 *
 * @example
 * ```ts
 * const touching = createRelation(RelationType.TOUCHING, { margin: 0.1 });
 * const supported = createRelation(RelationType.SUPPORTED_BY, { margin: 0.05, checkZ: true });
 * const coplanar = createRelation(RelationType.COPLANAR, { margin: 0.02, revNormal: false });
 * ```
 */
export function createRelation(
  type: RelationType,
  params?: Record<string, any>
): SpatialRelation {
  const p = params ?? {};

  switch (type) {
    case RelationType.TOUCHING:
      return new Touching(
        p.margin ?? 0.05,
        p.fromAbove ?? false,
        p.target ? new THREE.Vector3(p.target.x, p.target.y, p.target.z) : undefined
      );

    case RelationType.SUPPORTED_BY:
      return new SupportedBy(
        p.margin ?? 0.1,
        p.checkZ ?? true,
        p.supportPoint ? new THREE.Vector3(p.supportPoint.x, p.supportPoint.y, p.supportPoint.z) : undefined,
        p.supportHeight
      );

    case RelationType.COPLANAR:
      return new CoPlanar(
        p.margin ?? 0.05,
        p.revNormal ?? false,
        p.normal ? new THREE.Vector3(p.normal.x, p.normal.y, p.normal.z) : undefined,
        p.planePoint ? new THREE.Vector3(p.planePoint.x, p.planePoint.y, p.planePoint.z) : undefined
      );

    case RelationType.STABLE_AGAINST:
      return new StableAgainst(
        p.margin ?? 0.05,
        p.checkZ ?? true,
        p.revNormal ?? false,
        p.contactNormal ? new THREE.Vector3(p.contactNormal.x, p.contactNormal.y, p.contactNormal.z) : undefined,
        p.contactPoint ? new THREE.Vector3(p.contactPoint.x, p.contactPoint.y, p.contactPoint.z) : undefined
      );

    case RelationType.ROOM_NEIGHBOUR:
      return new RoomNeighbour(
        p.connectorTypes ?? ['door', 'open'],
        p.roomA,
        p.roomB
      );

    case RelationType.CUT_FROM:
      return new CutFrom(p.parentId);

    case RelationType.SHARED_EDGE:
      return new SharedEdge(p.margin ?? 0.01);

    case RelationType.TRAVERSE:
      return new Traverse(
        p.maxPathLength ?? Infinity,
        p.requireConnectorTypes ?? []
      );

    default:
      throw new Error(`Unknown relation type: ${type}`);
  }
}
