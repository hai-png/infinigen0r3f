/**
 * Spatial Relation Algebra
 *
 * Ports: infinigen/core/constraints/constraint_language/relations.py
 *
 * Defines a complete spatial relation algebra with:
 *  - Formal relation types (Touching, SupportedBy, CoPlanar, StableAgainst, etc.)
 *  - Implication lattice (which relations imply others)
 *  - Geometric constraint extraction (axis-aligned bounds for each relation)
 *  - Set algebra over relations: intersection, difference, negation
 *
 * This complements the existing Relation classes in ./relations.ts (which are
 * BoolExpression-based AST nodes for the constraint language) by providing
 * a pure algebraic layer for reasoning about spatial relations during
 * constraint solving — without needing a full AST evaluation context.
 *
 * The existing Relation hierarchy evaluates against a `Map<Variable, any>` state.
 * This module provides lightweight SpatialRelation objects that can:
 *  - Be checked for implication (does A imply B?)
 *  - Extract geometric constraints (bounds on position/orientation)
 *  - Be combined via intersection/difference (for domain pruning)
 *  - Be tested against a point+state (for feasibility checks)
 */

import * as THREE from 'three';

// ============================================================================
// Relation Type Enum
// ============================================================================

/**
 * Enumeration of all spatial relation types in the Infinigen constraint language.
 *
 * Each type corresponds to a spatial predicate that can hold between objects:
 * - TOUCHING: Objects are within a small margin of each other
 * - SUPPORTED_BY: Object A is vertically supported by object B
 * - COPLANAR: Objects lie on the same plane
 * - STABLE_AGAINST: Object A is stable when in contact with B
 * - ROOM_NEIGHBOUR: Rooms share a wall with a connector
 * - CUT_FROM: Object was generated by cutting from a parent
 * - SHARED_EDGE: Objects share a boundary edge
 * - TRAVERSE: A path exists between two rooms/regions
 */
export enum RelationType {
  TOUCHING = 'TOUCHING',
  SUPPORTED_BY = 'SUPPORTED_BY',
  COPLANAR = 'COPLANAR',
  STABLE_AGAINST = 'STABLE_AGAINST',
  ROOM_NEIGHBOUR = 'ROOM_NEIGHBOUR',
  CUT_FROM = 'CUT_FROM',
  SHARED_EDGE = 'SHARED_EDGE',
  TRAVERSE = 'TRAVERSE',
}

// ============================================================================
// Geometric Constraint Descriptor
// ============================================================================

/**
 * A geometric constraint extracted from a spatial relation.
 *
 * Describes an axis-aligned or directional bound on the position
 * of an object that must hold for the relation to be satisfied.
 */
export interface GeometricConstraint {
  /** Minimum value along the constraint axis */
  min: number;
  /** Maximum value along the constraint axis */
  max: number;
  /** Optional axis direction (defaults to the relation's natural axis) */
  axis?: THREE.Vector3;
}

// ============================================================================
// SpatialRelation Abstract Base
// ============================================================================

/**
 * Abstract base class for spatial relations in the relation algebra.
 *
 * Provides:
 *  - `implies`: Logical implication check (does this relation imply another?)
 *  - `satisfies`: Geometric test (does a point satisfy this relation?)
 *  - `intersects`: Set intersection (what's the overlap between two relations?)
 *  - `difference`: Set difference (this relation minus another)
 *  - `getConstraints`: Extract geometric bounds for constraint propagation
 *
 * The implication lattice (partial order):
 *   STABLE_AGAINST → SUPPORTED_BY → TOUCHING
 *   (If A is stable against B, then A is supported by B, then A is touching B)
 */
export abstract class SpatialRelation {
  /** The type of this relation */
  abstract readonly type: RelationType;

  /**
   * Whether this relation logically implies another relation.
   *
   * A implies B means: whenever A holds, B also holds.
   * For example, SUPPORTED_BY implies TOUCHING because
   * if an object is supported by another, it must be touching it.
   */
  abstract implies(other: SpatialRelation): boolean;

  /**
   * Whether a given point satisfies this spatial relation
   * given the current world state.
   *
   * @param point - The 3D position to test
   * @param state - Additional state (support surfaces, obstacles, etc.)
   */
  abstract satisfies(point: THREE.Vector3, state: any): boolean;

  /**
   * Compute the intersection of this relation with another.
   *
   * Returns a new relation that is satisfied only when BOTH
   * this relation AND the other are satisfied, or null if the
   * intersection is empty (the relations are mutually exclusive).
   */
  abstract intersects(other: SpatialRelation): SpatialRelation | null;

  /**
   * Compute the difference: this relation minus the other.
   *
   * Returns a relation satisfied when THIS holds but OTHER does not,
   * or null if this is a subset of other (difference is empty).
   */
  abstract difference(other: SpatialRelation): SpatialRelation | null;

  /**
   * Extract geometric constraints from this relation.
   *
   * Returns an array of axis-aligned bounds that any point satisfying
   * this relation must fall within. Used by the solver to prune
   * infeasible domain regions.
   */
  abstract getConstraints(): GeometricConstraint[];

  /**
   * Create a copy of this relation.
   */
  abstract clone(): SpatialRelation;
}

